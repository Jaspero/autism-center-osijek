			<h1>O nama</h1>
			<p><b>Naša misija</b> je suradnjom nastavnika, roditelja i lokalne zajednice svakom djetetu omogućiti razvitak u sredini u kojoj će se osjećati prihvaćeno, voljeno, sigurno i zadovoljno. Kreirati sredinu koja će djelovati poticajno na razvitak svih njegovih potencijala, u kojoj će se voditi briga o djetetovim specifičnostima kao pojedinca i gdje će ga se osposobljavati za samostalan i odgovoran život.</p>
			<p><b>Naša vizija</b> je odgoj i obrazovanje sretnog i uspješnog učenika koji se razvija uz poticaj učitelja, podršku roditelja i cijele lokalne zajednice.</p>
