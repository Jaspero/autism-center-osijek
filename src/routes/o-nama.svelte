<section class="grid p-y-xl">
	<div class="col-6 col-m-8 col-s-10 col-xs-12">
		<div class="read-format">
			<h1>Povijest centra</h1>
			<h3>16. svibnja 2015.</h3>
			<p>Na sastanku pročelnice Upravnog odjela za prosvjetu, kulturu, šport i tehničku kulturu Osječko-baranjske županije i defektologinje Sande Zlatarić predstavljena je ideja da se Škola za osposobljavanje i obrazovanje „Vinko Bek“ pretvori u Centar za autizam.</p>
			<h3>18. svibnja 2015.</h3>
			<p>Sanda Zlatarić, profesor rehabilitator, izabrana je za vršitelja dužnosti ravnatelja u Školi za osposobljavanje i obrazovanje „Vinko Bek“ te 19. svibnja 2015. preuzima navedenu dužnost.</p>
			<h3>29. svibnja 2015.</h3>
			<p>Ravnateljica Sanda Zlatarić šalje dopis Upravnom odjelu za prosvjetu, kulturu, šport i tehničku kulturu Osječko-baranjske županije s prijedlogom rješenja o pokretanju Centra za autizam koji je proslijeđen tadašnjem županu Vladimiru Šišljagiću.</p>
			<h3>4. lipnja 2015.</h3>
			<p>Na poziv ravnateljice, roditelji iz Inicijative za izgradnju Centra za autizam – Emilija Šmit Polić, Klaudija Scaggs i Daliborka Pavošević obilaze prostor Škole te su suglasni da zajedničkim snagama krenu u borbu za otvaranje budućeg Centra za autizam u Slavoniji, uz podršku osnivača škole Osječko-baranjske županije.</p>
			<h3>19. lipnja 2015.</h3>
			<p>U Upravnom odjelu za prosvjetu, kulturu, šport i tehničku kulturu Osječko-baranjske županije održan je radni sastanak na kojem su dogovoreni koraci za realizaciju ideje otvaranja Centra za autizam.</p>
			<h3>26. lipnja 2015.</h3>
			<p>U prostoru Škole održan je sastanak na temu broja djece i odraslih korisnika s autizmom koji bi bili polaznici budućeg Centra za autizam.</p>
			<h3>29. lipnja 2015.</h3>
			<p>U Upravnom odjelu za prosvjetu, kulturu, šport i tehničku kulturu Osječko-baranjske županije održan je sastanak s temom osnivanja Centra za autizam u sklopu Škole za osposobljavanje i obrazovanje „Vinko Bek“ u Osijeku.</p>
			<h3>6. srpnja 2015.</h3>
			<p>U Gradu Osijeku, u prostorijama Velike vijećnice održan je sastanak na temu rješavanja problematike djece i odraslih osoba s autizmom u Osijeku i regiji.</p>
			<h3>22. rujna 2015.</h3>
			<p>U prostorijama Upravnog odjela za prosvjetu, kulturu, šport i tehničku kulturu Osječko-baranjske županije održan je sastanak u svezi arhitektonskog rješenja nove zgrade Centra za autizam.</p>
			<h3>10. listopada 2015.</h3>
			<p>Između tri ministarstva, županije osječko-baranjske i Grada Osijeka potpisan je Sporazum o suradnji u uspostavi Centra za odgoj, obrazovanje i rehabilitaciju Osijek.</p>
			<h3>16. prosinca 2015.</h3>
			<p>Potpisan je dodatak Sporazumu.</p>
			<h3>17. prosinca 2015.</h3>
			<p>Osnivač Škole donosi odluku o promjeni naziva i djelatnosti Škole za osposobljavanje i obrazovanje „Vinko Bek“ u  Osijeku i upućuje je MZOS-u.</p>
			<p>Tijekom mjeseca, ravnateljica Sanda Zlatarić izrađuje Studij opravdanosti izgradnje zgrade Centra za odgoj, obrazovanje i rehabilitaciju Osijek. U međuvremenu MZOS traži doradu Odluke o promjeni imena i djelatnosti škole te isto šalje Osnivaču.</p>
			<h3>20. srpnja 2016.</h3>
			<p>Osnivač donosi Rješenje o javnoj nabavi izgradnje Centra za odgoj, obrazovanje i rehabilitaciju Osijek.</p>
			<h3>26. kolovoza 2016.</h3>
			<p>Javno otvaranje ponuda.</p>
			<h3>7. rujna 2016.</h3>
			<p>Poništenje postupka javne nabave za izgradnju Centra za odgoj, obrazovanje i rehabilitaciju Osijek.</p>
			<h3>30. rujna 2016.</h3>
			<p>Na zahtjev župana Vladimira Šišljagića traže se pojašnjenje kvadrature te zakonske odrednice zadanog prostora budućeg centra. Ravnateljica izrađuje studiju prostora.</p>
			<h3>5. listopada 2016.</h3>
			<p>Održana je skupština Osječko-baranjske županije  na kojoj je donesena odluka o pokretanju javne nabave za izvođače radova.</p>
			<h3>18. listopada 2016.</h3>
			<p>U Županijskom glasniku broj 7/16 objavljena je Odluka o promjeni imena i djelatnosti Škole za osposobljavanje i obrazovanje „Vinko Bek“ Osijek. Odluka postaje pravomoćna 26. listopada 2016. godine.</p>
			<h3>31. listopada  2016.</h3>
			<p>Objavljen je natječaj za izgradnju Centra za odgoj obrazovanje i rehabilitaciju Osijek koji je otvoren do 21. studenog 2016. godine. Po nalogu Odjela za prosvjetu Osječko-baranjske županije Škola je, uz suglasnost Školskog odbora, otvorila natječaj za odabir nadzora građenja.</p>
			<h3>17. siječnja 2017.</h3>
			<p>Potpisan je ugovor s odabranim izvođačem za izgradnju Centra, tvrtkom ZUBER DOO. Predviđeni je rok radova 2 godine i 5 mjeseci uz mogućnost rebalansa i ranijeg završetka. Uredbom župana vođenje investicije izgradnje Centra preuzeo je Odjel za gospodarstvo i regionalni razvoj Osječko-baranjske županije te je 24. siječnja 2017. godine održan sastanak koordinacije predstavnika Odjela za gospodarstvo i regionalni razvoj Osječko-baranjske županije, nadzora i ravnateljice Centra.</p>
			<h3>19. stunenog 2018.</h3>
			<p>Nakon završetka izgradnje Centra obavljen je tehnički pregled građevine te je 19. studenog 2018. godine izdana Uporabna dozvola za istu građevinu. Nakon toga započelo se sa opremanjem Centra. Skupština Osječko-baranjske županije  na sjednici održanoj 13. prosinca 2018. godine donijela je Odluku o promjeni naziva i djelatnosti Centra za odgoj, obrazovanje i rehabilitaciju, a  Ministarstvo znanosti i obrazovanja je 22. siječnja 2019. godine donijelo Rješenje o  sukladnosti ove odluke sa zakonom. Ovom odlukom novi naziv centra je Centra za autizam. Ministarstvo znanosti i obrazovanja je 20. veljače 2019. godine izdalo Rješenje da centar za autizam, Osijek ispunjava uvjete propisane zakonom za rad u promijenjenim uvjetima u novoizgrađenom objektu.</p>
			<p>Nakon dobivanja potrebnih suglasnosti za 22 radna mjesta od Ministarstva znanosti i obrazovanja, tijekom veljače 2019. god., provodi se natječajni postupak za nova radna mjesta i vrši se zapošljavanje prema provedenom natječajnom postupku.</p>
			<h3>01. ožujka 2019.</h3>
			<p>Od 01. ožujka 2019. godine Centar za autizam započinje s radom u okviru djelatnosti vezanih za rad s djecom i učenicima s poremećajima iz spektra autizma. Tijekom veljače i ožujka 2019. godine u tijeku je izrada i usvajanje Statuta Centra za autizam. Skupština Osječko-baranjske županije  na sjednici održanoj 04. travnja 2019. godine donijela je Odluku o davanju prethodne suglasnosti na Statut Centra za autizam, Osijek, slijedom koje je Školski odbor Centra donio Odluku o usvajanju Statuta Centra za autizam, Osijek.</p>
			<h3>29. travnja 2019.</h3>
			<p>Nakon stupanja na snagu Statuta , trgovačkom sudu u Osijeku ponesen je zahtjev za promjenu naziva i djelatnosti. Rješenjem Trgovačkog suda u Osijeku od 29. travnja 2019. godine (Tt-19/2638-2) Centar za odgoj, obrazovanje i rehabilitaciju promijenio je naziv u Centar za autizam.</p>
