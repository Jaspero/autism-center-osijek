				<h1>OSIgurajmo im JEdnaKost 4</h1>
				<p>Projekt OSIgurajmo im JEdnaKost 4 sufinanciran je sredstvima Europske unije iz Europskog socijalnog fonda, a vodi ga Grad Osijek zajedno s partnerima.</p>
				<p>Projektom je omogućena stručna podrška pomoćnika u nastavi učenicima s teškoćama u razvoju, kojima su najpotrebniji.</p>
				<p>Cilj je projekta osigurati uvjete za poboljšanje inkluzivnog obrazovanje djece s teškoćama u razvoju pružanjem podrške i potpore na područjima učenja, socijalizacije i emocionalnog funkcioniranja putem uvođenja pomoćnika u nastavi.</p>
				<p>Brojna istraživanja potvrđuju kako su upravo pomoćnici u nastavi najbolji oblik izravne potpore učenicima s teškoćama pri svladavanju nastavnoga gradiva i socijalnih barijera na putu prema samostalnosti i ravnopravnosti.</p>
				<p>Svojim će radom pomoćnici u nastavi pružati potporu emocionalnom razvoju učenika s teškoćama, pomoći pri poboljšanju njihovih obrazovnih postignuća i kompetencija te biti konstantna podrška pri razvijanju njihovih socijalnih vještina. Također će doprinijeti njihovu osamostaljivanju i ravnopravnom uključivanju u društvo.</p>
				<p>Projekt "OSIgurajmo im JEdnaKost 4" provodi se od 1. rujna 2017. i trajat će do 15. kolovoza 2021. godine. Centar za autizam uključen je u projekt od travnja 2019.</p>
				<p>Više informacija o samom projektu dostupno je na službenoj stranici: <a href="http://osigurajmoimjednakost.com" target="_blank" rel="noopener">http://osigurajmoimjednakost.com</a>.</p>
			</article>
