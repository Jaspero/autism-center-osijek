				<h1>Asistent u nastavi</h1>
				<p>Pomoćnike u vrtiću osigurava Grad Osijek na temelju zahtjeva koji zajedno s potrebnom dokumentacijom podnosi Centar.</p>
				<p>Uloga pomoćnika u vrtiću podrazumijeva:</p>
				<ul>
					<li>Produženu ruku djetetu u okolini u kojoj boravi,</li>
					<li>Usmjeravanje djeteta na drugo dijete/djecu u skupini ili na odgajatelja, tj. pomoć u ostvarivanju djetetovih socijalnih interakcija,</li>
					<li>Osiguravanje djetetove dodatne pažnje i usmjerenosti na sadržaje i aktivnosti koje se oko njega događaju,</li>
					<li>Pomaganje djetetu pri izvršenju zadataka,</li>
					<li>Nagrađivanje djeteta za dobro obavljene zadatke,</li>
					<li>Interveniranje u sprječavanju djetetovog nepoželjnog ponašanja,</li>
					<li>Pomaganje odgajatelju pri uključivanju djeteta u skupinu i aktivnosti koje se u skupini provode.</li>
				</ul>
				<p>Pomoćnik vodi, educira i prati dijete uz stručnu podršku i pomoć odgojitelja, s ciljem poticanja djetetova razvoja.</p>
				<p>Centar za autizam uključen je u projekt od ožujka 2019.</p>
