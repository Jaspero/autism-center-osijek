				<h1>Primjena tehnologije 21. stoljeća za promociju komunikacije, obrazovanja i socijalnog uključivanja djece rane dobi s teškoćama u razvoju</h1>
				<p>Centar za autizam uključio se u projekt Primjena tehnologije 21. stoljeća za promociju komunikacije, obrazovanja i socijalnog uključivanja djece rane dobi s teškoćama u razvoju. Projekt se provodi u suradnji Unicefa, Edukacijsko rehabilitacijskog fakulteta i Fakulteta elektrotehnike i računarstva. Program će trajati 17 mjeseci, a početak je svibanj 2019. godine.</p>
				<p>Glavni ciljevi programa su:</p>
				<ul>
					<li>Jačanje kapaciteta stručnjaka i institucija u okviru zdravstvenog, obrazovnog i socijalnog sustava te civilnog društva za sustavnu primjenu potpomognute komunikacije u radu s djecom sa složenim komunikacijskim potrebama od 0 do 8 godina i njihovim obiteljima,</li>
					<li>Unaprjeđenje dostupnosti asistivne tehnologije i aplikacija za potpomognutu komunikaciju za djecu sa složenim komunikacijskim potrebama od 0 do 8 godina i njihovim obiteljima.</li>
				</ul>
				<p>Predviđene programske aktivnosti uključuju:</p>
				<ul>
					<li>Edukaciju stručnjaka za primjenu potpomognute komunikacije, uključujući online edukaciju te trening u trajanju od 6 dana (2 x 3 dana),</li>
					<li>Prilagodba/izrada simbola i testiranje hrvatske verzije aplikacije Cboard (<a href="https://www.cboard.io/" target="_blank" rel="noopener">https://www.cboard.io/</a>) za potpomognutu komunikaciju.</li>
				</ul>
