				<h1>Volontiranje</h1>
				<p>Volonterstvo (volontiranje, volonterski/dobrovoljni rad) u najširem značenju podrazumijeva rad pojedinca na dobrobit drugih pri čemu je njegovo djelovanje slobodno odabrano, neprofitno i bez financijske dobiti.</p>
				<p>Volonter je osoba koja dragovoljno i besplatno obavlja neki rad ili aktivnost izvan svog redovitog zanimanja u određenoj trajnosti za dobro drugih osoba, neke zajednice ili društva općenito. Zakonom o volonterstvu (NN 58/2007) volontiranje je prepoznato kao aktivnost koja dovodi do poboljšavanja kvalitete života, do aktivnog uključivanja osoba u društvena zbivanja te do razvoja humanijega i ravnopravnijega demokratskog društva.</p>
				<p>Prema Europskom manifestu o volontiranju,  volontiranje je aktivnost koja se poduzima:</p>
				<ul>
					<li>Slobodnom voljom pojedinca te osobnim izborom i motivacijom,</li>
					<li>U organiziranom okruženju (unutar nevladinih udruga, volonterskih centara, manje ili više organiziranih neformalnih skupina),</li>
					<li>U želji da se pomogne drugim osobama i društvu u cjelini, pridonoseći zajedničkim vrijednostima, bez osobnih interesa (iako je volontiranje od jednake koristi i samoj osobi koja volontira),</li>
					<li>Bez želje za financijskom dobiti (izvor Volonterski Centar Osijek).</li>
				</ul>
				<p>Općom deklaracijom o volonterstvu volontiranje je jedan od kamena temeljaca civilnoga društva, jer oživljava najplemenitija stremljenja čovječanstva zauzimanje za mir, slobodu, mogućnost izbora, sigurnost i pravičnost za sve ljude.</p>
				<p>Zašto volontirati u našem Centru?</p>
				<ul>
					<li>Razvijaš svijest o potrebama drugih,</li>
					<li>Stječeš nova znanja i vještine,</li>
					<li>Upoznaješ nove ljude,</li>
					<li>Ističeš vrijednost rada s najpotrebijima,</li>
					<li>Utječeš na senzibilizaciju zajednice i društva,</li>
					<li>Daješ glas onima kojima nije lako izraziti što misle, osjećaju,</li>
					<li>Smiješ se i plačeš zajedno s nama,</li>
					<li>Razlog si za svaki korak naprijed…i oslonac za korak unazad,</li>
					<li>Učiš o sebi i drugima,</li>
					<li>Iskustvo je neprocjenjivo, a zadovoljstvo neopisivo,</li>
					<li>Osjećaš se dobro čineći dobro,</li>
					<li>Mi osjećamo da možemo SVE jer si uz nas.</li>
				</ul>
				<p>Hvala ti…</p>
